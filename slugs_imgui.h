void slimgui_frame(){
    slg_framebuffer_t frame_buf = slg_d3d12_state.frame_buf;
    //slg_app_data_t* app_data = &slg_d3d12_state.appdata;
    io->MousePos = (ImVec2){(float)slimgui_input_state.mouse_x,(float)slimgui_input_state.mouse_y};
    io->MouseDown[0] = slimgui_input_state.left_mouse_down;
    io->MouseDown[1] = slimgui_input_state.right_mouse_down;
    igNewFrame();

    igShowDemoWindow(NULL);

    igRender();

    ImDrawData* draw_data = igGetDrawData();
    
    ID3D12GraphicsCommandList* command_list = frame_buf.frame_context[frame_buf.frameIndex].commandList;   

    //so we need to count up all the verts and indexes in all of the draw data??

    size_t total_verts = 0;
    size_t total_index = 0;
    for(int i = 0;i<draw_data->CmdListsCount;i++){
        ImDrawList* list = draw_data->CmdLists.Data[i];
        total_verts += list->VtxBuffer.Size;
        total_index += list->IdxBuffer.Size;
    }


    //COME BACK TO THIS. NOT SURE IF I WANT TO HAVE A DYNAMIC ALLOCATION EVERY FRAME LIKE THIS
    ImDrawVert* final_verts = malloc(total_verts * sizeof(ImDrawVert));
    ImDrawIdx* final_index = malloc(total_index * sizeof(ImDrawIdx));
    ImDrawVert* vert_ptr = final_verts;//going to be using this to index into the array
    ImDrawIdx* idx_ptr = final_index;
    for(int i = 0;i<draw_data->CmdListsCount;i++){
        
        ImDrawList* list = draw_data->CmdLists.Data[i];

        ImDrawVert* verts = list->VtxBuffer.Data;
        size_t current_vertex_size = list->VtxBuffer.Size;
       

        ImDrawIdx* index = list->IdxBuffer.Data;
        size_t current_index_size = list->IdxBuffer.Size;
        
        memcpy(vert_ptr, verts, current_vertex_size * sizeof(ImDrawVert));
        memcpy(idx_ptr, index, current_index_size * sizeof(ImDrawIdx));
        vert_ptr += current_vertex_size;
        idx_ptr += current_index_size;

       
        
    }
    //update current frame buffers with the corresponding information
    //command_list->lpVtbl->RSSetScissorRects(command_list,1,&scissor_rect);
    slg_update_buffer(slimgui_frame_buffer.index_buffers[frame_buf.frameIndex],(void*)final_index,(unsigned int)total_index * sizeof(ImDrawIdx));
    slg_update_buffer(slimgui_frame_buffer.vertex_buffers[frame_buf.frameIndex],(void*)final_verts,(unsigned int)total_verts * sizeof(ImDrawVert));
    slg_set_pipeline(&slimgui_pip);
    slg_set_bindings(&slimgui_frame_buffer.bind[frame_buf.frameIndex]);
    size_t current_index_draw = 0;
    size_t current_vertex_draw = 0;
    //temp
    /*DXGI_SWAP_CHAIN_DESC sc;
    slg_d3d12_state.frame_buf.swapchain->lpVtbl->GetDesc(slg_d3d12_state.frame_buf.swapchain,&sc);
    ID3D12Resource* backbuffer = NULL;

    HRESULT hr = slg_d3d12_state.frame_buf.swapchain->lpVtbl->GetBuffer(
        slg_d3d12_state.frame_buf.swapchain,
        slg_d3d12_state.frame_buf.frameIndex,                 // 0 .. bufferCount-1
        &IID_ID3D12Resource,
        (void**)&backbuffer
    );
    D3D12_RESOURCE_DESC desc;
    backbuffer->lpVtbl->GetDesc(backbuffer,&desc);

    UINT bb_width  = (UINT)desc.Width;
    UINT bb_height = desc.Height;   // Height is UINT
    ImVec2 display_size = io->DisplaySize;*/
    //
    RECT rect;
    GetClientRect(app_data_d3d12.hwnd, &rect);
    for(int i = 0;i<draw_data->CmdListsCount;i++){
        ImDrawList* list = draw_data->CmdLists.Data[i];
        ImVec2 clip_off = draw_data->DisplayPos;
        for(int n  = 0;n<list->CmdBuffer.Size;n++){
            
            ImDrawCmd cmd = list->CmdBuffer.Data[n];
            

            D3D12_RECT scissor_rect;
            scissor_rect.left   = (LONG)(cmd.ClipRect.x - clip_off.x);
            scissor_rect.top    = (LONG)(cmd.ClipRect.y - clip_off.y);
            scissor_rect.right  = (LONG)(cmd.ClipRect.z - clip_off.x);
            scissor_rect.bottom = (LONG)(cmd.ClipRect.w - clip_off.y);
            command_list->lpVtbl->RSSetScissorRects(command_list,1,&scissor_rect);
            D3D12_VIEWPORT viewport = {
                0.0f, 0.0f,
                io->DisplaySize.x,
                io->DisplaySize.y,
                0.0f, 1.0f
            };
            command_list->lpVtbl->RSSetViewports(command_list,1,&viewport);
            //set the bindings and draw

            
            //this should be the proper draw call
            slg_draw((unsigned int)cmd.ElemCount,1,(unsigned int)cmd.IdxOffset + (unsigned int)current_index_draw,(unsigned int)current_vertex_draw,0);

            //in here we would then set the scissor rect
            //bind the call
            //we probably only need one pipeline and one shader
            //draw
        }
        current_index_draw += list->IdxBuffer.Size;
        current_vertex_draw += list->VtxBuffer.Size;
        
    }
    free(final_verts);
    free(final_index);
}

void slimgui_end_frame(){
    slg_submit_draw();
}



